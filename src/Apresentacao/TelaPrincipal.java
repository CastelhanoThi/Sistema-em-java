    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        Date datasistema = new Date();
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        jLdata.setText(formato.format(datasistema));
        
        Timer timer = new Timer(1000, new Hora());
        timer.start();
    }//GEN-LAST:event_formWindowOpened

    private void jMlogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMlogarActionPerformed
        int opcao;
        opcao = JOptionPane.showConfirmDialog(null,"Deseja entrar com outro usuario",
                "Fechar",JOptionPane.YES_NO_OPTION);
        if(opcao == JOptionPane.YES_NO_OPTION){
            TelaLogin login = new TelaLogin();
            login.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jMlogarActionPerformed

    Apresentacao.TelaClientes clientes;
    private void jMCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMCadastroActionPerformed
        if(estaFechado(clientes)){
            clientes = new TelaClientes(jLusuario.getText());
            jDesktop.add(clientes).setLocation(150, 30);
            clientes.show();
            clientes.toFront();
        }else{
            clientes.show();
            clientes.toFront();
        }
    }//GEN-LAST:event_jMCadastroActionPerformed

    Apresentacao.TelaTabela tabela;
    private void jMtabelaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMtabelaActionPerformed
        if(estaFechado(tabela)){
            tabela = new TelaTabela(jLusuario.getText());
            tabela = new TelaTabela(jLusuario.getText());
            tabela.enviar(id_tabela);
            jDesktop.add(tabela).setLocation(150, 30);
            tabela.show();
        }else{
            tabela.show();
            tabela.toFront();
        }
    }//GEN-LAST:event_jMtabelaActionPerformed

