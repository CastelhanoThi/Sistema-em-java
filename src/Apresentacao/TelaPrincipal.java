    public boolean estaFechado(Object obj){
        JInternalFrame[] ativo = jDesktop.getAllFrames();
        boolean fechado = true;
        int i = 0;
        
        while(i < ativo.length && fechado){
            if(ativo[i] == obj){
                fechado = false;
            }
            i++;
        }
        return fechado;
    }
    
